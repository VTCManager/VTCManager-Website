<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">Entwicklung</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">Bereits vor 2 Monaten habe ich dieses Projekt ins Leben gerufen. Die Hauptidee war eine neue Speditionsverwaltungssoftware zu erschaffen, die ein neues Maß an Komfort, Leistung und Features bieten soll. Für mich waren diese 2 Monate Entwicklungszeit bis zum Release harte Arbeit. Ich habe neue Programmiersprachen erlernt, Rückschlage erlitten, aber nie aufgegeben. Auch Dank meinen Freunden. Ich stand kurz davor einer anderen Speditionsverwaltungssoftware beizutreten, da ich an dem Code alleine arbeiten musste und ein Team beitreten wollte um mich besser austauschen zu können, doch sie haben mich glücklicherweise davon abgehalten, da sonst niemand mehr was mit meinem Code hätte anfangen können.</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">
  <br>
</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">Release</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">Diesen Freitag wird der VTCManager nun offiziell erscheinen. Es gibt noch ein paar Ding zu tun bis dahin, aber im Großen und Ganzen ist er bereit zum Release. In den kommenden Wochen werden wir die Webseite updaten, um fehlende Features dort zu ergänzen.</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">Unerwartet kam die Aufgabe von TruckingVS. Dadurch haben wir zwar zahlreiche Nutzer dazubekommen, mussten aber das Launch-Datum nach vorne verlegen. Ich hoffe, dass am Freitag alles gut gehen wird. Zudem unterstützt MoshpitJim ab jetzt tatkräftig das Projekt als Community Manager.</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">
  <br>
</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">Hinweise:</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">1. Abschließen der Fahrt: Nach dem „Rückblick auf die Fahrt“-Dialog in ETS2 auf „Fahren“ im Hauptmenü klicken. Wenn ein Ton zu hören ist, hat alles funktioniert</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">2.Eigene Trailer: Eigene Trailer müssen nach dem „Rückblick auf die Fahrt“-Dialog in ETS2 sofort abgekoppelt und wieder angekoppelt werden, damit VTCManager die Fahrt abschließt.</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">3. Eigene Trailer2: Solltet ihr mit einem eigenen Trailer zu einer Firma fahren, dann aber einen Auftrag mit einem Trailer der Firma starten, müsst ihr euren eigenen Trailer vor dem Annehmen des Auftrages abkoppeln. Ansonsten muss der Auftrag wieder abgebrochen (wenn im VTCManager keine Fahrt mehr sichtbar ist) und unter Umständen muss VTCManager und/oder ETS2 neu gestartet werden.</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">4. Bugs: Trotz eines umfassenden PreRelease-Programm werden höchstwahrscheinlich einige Bugs (aufgrund des vorgezogenen Launch-Termines) zu finden sein. Falls ihr einen Bug findet, schreibt mir über Discord, dass ihr ein EasterEgg gefunden habt (samt Screenshot bitte). Wir werden den Bug so schnell wie möglich beheben.</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">
  <br>
</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">Ausblick</p>
<p style="margin-bottom: 0cm;line-height: 100%;background: transparent;">VTCManager ist lange noch nicht fertig. Es fehlen noch zahlreiche Basic-Funktionen, Konfigurations-Einstellungen und Extra-Features. Angedacht von mir war ein eventueller Release eines TrafficDetectionService, ein Algorithmus, der Verkehrsbehinderungen im Multiplayer erkennt und per Overlay-Benachrichtigung (Overwolf) Nutzern mitteilt, die auf der Strecke unterwegs sind. Interessant wird es auch, wenn es um die Thematik DIY geht. Ich habe zuhause noch ein VW Polo 6 Cluster mit einem Arduino rumliegen. Eventuell könnte es dafür eine Einbindung geben. Oder ein besseres Navigationssystem für den RaspberryPI mit VTCManager-Integration. Alles interessante Features, die vielleicht bald erscheinen werden.</p>
